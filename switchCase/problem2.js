let string = "3";
switch (string) {
    case "1":
        console.log("Tamil");
        break;
    case "2":
        console.log("English");
        break;
    case "3":
        console.log("Maths");
        break;
    case "4":
        console.log("Science");
        break;
    case "5":
        console.log("Social");
        break;
    default:
        console.log("Invalid");
        break;
}