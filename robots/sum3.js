// A robot can move in 4 directions: North, South, East, and West. 
// Use a switch statement to make the robot move in the direction entered by the user and 
// print the robot's new position after each movement.
let direction = 3;
switch (direction) {
    case 1:
        console.log("North");
        break;
    case 2:
        console.log("South");
        break;
    case 3:
        console.log("East");
        break;
    case 4:
        console.log("West");
        break;
    default:
        console.log("Invalid");
        break;

}

